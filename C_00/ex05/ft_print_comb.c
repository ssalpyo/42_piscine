#include <unistd.h>

void	ft_print_comb(void)
{
	int		a;
	char	b;
	char	c;
	char	d;

	a = 12;
	while (a <= 789)
	{
		b = a / 100 + '0';
		c = (a / 10) % 10 + '0';
		d = a % 10 + '0';
		if (b < c && c < d)
		{
			write(1, &b, 1);
			write(1, &c, 1);
			write(1, &d, 1);
			if (a < 789)
			{
				write(1, ",", 1);
				write(1, " ", 1);
			}
		}
		a++;
	}
}
